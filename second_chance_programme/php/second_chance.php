            <p class="fs-5" style="color: grey;">Information about the Second Chance Matric Support Programme</p>
            <div class="">
                <ul>
                    <li>The Second Chance Matric Support Programme (SCMSP) is offered by the Department of Basic Education
                        to assist National Senior Certificate (NSC) and Senior Certificate (SC) part-time candidates to prepare
                        for the May/June and October/November examinations. It is important to note that SC part-time candidates
                        will only be allowed to write the May/June examination and cannot register to write the October/November 
                        examination.
                    </li>
                    <li>The centres will provide support to part-time candidates who have not been able to meet the requirements of the National Senior Certificate (NSC)
                        or the amended Senior Certificate (SC).
                    </li>
                    <li>On the DBE platform, you will also have access to study material <b>(past question papers, study guides, and videos)</b> covering the targeted subjects.</li>
                </ul>
                <a class="btn btn-primary" href="https://www.education.gov.za/Programmes/SecondChanceProgramme.aspx" role="button" style="border-radius: 0%;">DBE: Second Chance Information</a>
            </div>

            <p class="mt-3 fs-5"><b>Benefits:</b></p>
            <div class="">
              <ul>
                <li>Enables learners to meet the requirements of the NSC or SC.</li>
                <li>Increases learner retention and access to further education opportunities.</li>
                <li>Enhances computer skills, job prospects, and career pathways.</li>
                <li>Provides access to bursary opportunities for further studies.</li>
              </ul>
            </div>

